namespace FriendsOfHyperf\ValidatedDTO;

use Countable;

/**
 * Determine if the given value is "blank".
 *
 * @param mixed $value
 */
function blank($value): bool
{
    if (is_null($value)) {
        return true;
    }

    if (is_string($value)) {
        return trim($value) === '';
    }

    if (is_numeric($value) || is_bool($value)) {
        return false;
    }

    if ($value instanceof Countable) {
        return count($value) === 0;
    }

    return empty($value);
}
